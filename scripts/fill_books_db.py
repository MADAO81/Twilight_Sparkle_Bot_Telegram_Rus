def fill_db():
    """Наполняет базу данных книгами."""
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    inserted = 0
    for book in BOOKS:
        cursor.execute("""
            INSERT INTO books (title, author, description, genre, source)
            VALUES (?, ?, ?, ?, ?)
        """, (
            book['title'],
            book.get('author', 'Неизвестен'),
            book['description'],
            book.get('genre', 'приключения'),
            'база'
        ))
        inserted += 1

    conn.commit()
    conn.close()
    print(f"✅ Добавлено {inserted} книг в базу данных: {DB_PATH}")


if __name__ == "__main__":
    fill_db()